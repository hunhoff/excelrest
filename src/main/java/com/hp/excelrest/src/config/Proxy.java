package com.hp.excelrest.src.config;

public class Proxy {

	private String host;
	private String port;
	
	public Proxy(){}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public String getPort() {
		return port;
	}

	public void setPort(String port) {
		this.port = port;
	}
	
}
	
